    /**
     * Llena la tabla con todos los datos sin discriminar por tipo de unidad.
     */
        private void llenarTablaInicial() throws ParseException{
        DefaultTableModel modelo = new DefaultTableModel(0,0);
        TablaClientes llenar = new TablaClientes();
        Vector listaTitulos = llenar.tituloColumnas();
        Vector filas = llenar.elementosCelda();
        
        modelo.setDataVector(filas, listaTitulos); 
        tblClientes.setModel(modelo);
    }
        /**
         * Metodo que llena la tabla de los Deudores
         * @throws ParseException se arroja cuando no funciona el metodo elemtosCeldaDeudores
         */
        private void llenarTablaDeudores() throws ParseException{
        DefaultTableModel modelo = new DefaultTableModel(0,0);
        TablaClientes llenar = new TablaClientes();
        Vector listaTitulos = llenar.tituloColumnas();
        Vector filas = llenar.elementosCeldaDeudores();
        
        modelo.setDataVector(filas, listaTitulos); 
        tblClientes.setModel(modelo);
        }
        /**
         * Lee las opciones consultadas del grupo de botones de los clientes
         * @return un String con el valor
         */
        private String leerProcedenciaSeleccionado(){
        Enumeration<AbstractButton> botonElegido= buttonGroup1.getElements();
        String seleccionado = "";
        while(botonElegido.hasMoreElements()){
            
            JRadioButton rbAux =(JRadioButton)botonElegido.nextElement();
            if(rbAux.isSelected()){
                //System.out.println(rbAux.getText());
                seleccionado = rbAux.getText();
                break;
            }
        
        }
        return seleccionado;
        
    
    }   
    /**
     * Metodo que junta los radio butons de clientes y clientes deudores
     */ 
    private void juntaRbClientes(){
    
        buttonGroup1.add(rbTodos);
        buttonGroup1.add(rbDeudores);
    }
