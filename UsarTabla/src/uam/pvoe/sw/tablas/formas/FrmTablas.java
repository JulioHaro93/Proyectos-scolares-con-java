    private void  generarTablaDinamica(int nF, int nC){
        OperacionesTabla operaciones = new OperacionesTabla();
        Vector cols = operaciones.generaColumnas(nC);
        Vector filas= operaciones.generaFilas(nF, nC);
        
        DefaultTableModel modelo = new DefaultTableModel(0,0);
        modelo.setDataVector(filas, cols);
        tblTablaDinamica.setModel(modelo);

    }
   
