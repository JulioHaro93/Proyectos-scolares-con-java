    private void generarTabla(){
        Vector columnas = new Vector();
        columnas.add("Columna A");
        columnas.add("Columna B");
        columnas.add("Columna C");
        columnas.add("Columna D");
        columnas.add("Columna E");
        
        
        Vector todasLasFilas = new Vector();
        Vector fila = new Vector();
        
        
        fila.add("1_1");
        fila.add("1_2");
        fila.add("1_3");
        fila.add("1_4");
        fila.add("1_5");
        
        todasLasFilas.add(fila);
        
        fila = new Vector();
        
        fila.add("2_1");
        fila.add("2_2");
        fila.add("2_3");
        fila.add("2_4");
        fila.add("2_5");
   
        todasLasFilas.add(fila);
        
        DefaultTableModel modelo = new DefaultTableModel(0,0);
        modelo.setDataVector(todasLasFilas, columnas);
        
        tblTablaSimple.setModel(modelo);
    }

